export async function loadIcon(name: string): Promise<string> {
    if (INLINE_ICONS[name]) return INLINE_ICONS[name];
    if (iconCache[name]) {
        return iconCache[name];
    }

    try {
        const response = await fetch(`./assets/icons/${name}.svg`);
        if (!response.ok) throw new Error(response.statusText);
        const svg = await response.text();
        iconCache[name] = svg;
        return svg;
    } catch (error) {
        console.error(`Failed to load icon: ${name}`, error);
        return INLINE_ICONS[name] || '<svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2"><circle cx="12" cy="12" r="10"></circle><line x1="12" y1="8" x2="12" y2="12"></line><line x1="12" y1="16" x2="12.01" y2="16"></line></svg>';
    }
}

export function createIconElement(name: string, size: number = 20): HTMLElement {
    const wrapper = document.createElement('span');
    wrapper.className = 'icon-wrapper';
    wrapper.style.display = 'inline-flex';
    wrapper.style.alignItems = 'center';
    wrapper.style.justifyContent = 'center';
    wrapper.style.width = `${size}px`;
    wrapper.style.height = `${size}px`;

    loadIcon(name).then(svg => {
        wrapper.innerHTML = svg;
        const svgEl = wrapper.querySelector('svg');
        if (svgEl) {
            svgEl.setAttribute('width', size.toString());
            svgEl.setAttribute('height', size.toString());
        }
    });

    return wrapper;
}